// ================================================================
// Language detection + switching
// ================================================================

let currentLang = 'en';

async function detectLanguageByIP() {
  try {
    // Try ipapi.co first
    const res = await fetch('https://ipapi.co/json/', { signal: AbortSignal.timeout(4000) });
    const data = await res.json();
    const country = data.country_code || '';
    const cnCountries = ['CN', 'TW', 'HK', 'MO', 'SG', 'MY'];
    return cnCountries.includes(country) ? 'cn' : 'en';
  } catch {
    try {
      // Fallback: browser language
      const bl = navigator.language || navigator.userLanguage || 'en';
      return bl.startsWith('zh') ? 'cn' : 'en';
    } catch {
      return 'en';
    }
  }
}

function t(key) {
  return I18N[currentLang][key] || I18N.en[key] || key;
}

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('opentree_lang', lang);

  // Update body class for font switching
  document.body.classList.toggle('lang-cn', lang === 'cn');

  // Update lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });

  // Update all placeholders with data-i18n-ph
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });

  // Re-render marquee
  renderMarquee();

  // Fire custom event for page-specific updates
  document.dispatchEvent(new CustomEvent('langChanged', { detail: { lang } }));
}

function renderMarquee() {
  const track = document.getElementById('marqueeTrack');
  if (!track) return;
  const items = t('marquee');
  const html = [...items, ...items].map(item =>
    `<span class="marquee-item">${item}</span>`
  ).join('');
  track.innerHTML = html;
}

async function initLang() {
  // Priority: localStorage > IP detection
  const saved = localStorage.getItem('opentree_lang');
  if (saved) {
    applyLang(saved);
  } else {
    const detected = await detectLanguageByIP();
    applyLang(detected);
  }
}

// Expose for inline usage
window.switchLang = function(lang) { applyLang(lang); };

document.addEventListener('DOMContentLoaded', initLang);
